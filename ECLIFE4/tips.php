                                       <h2>Why Rice?</h2>
                                       <p>Rice is a starchy grain used as a staple ingredient by more than half the world's population largely due to its versatility, availability, and ability to adapt to any flavor and seasoning. Rice has a chewiness and soft texture that adds substance to meals and complements many cuisines.</p>

                                       <h3>Nutrients</h3>
                            <p>There are over 53 grams of carbohydrate in a single serving of white rice. Only a tiny amount of that carbohydrate comes from fiber. Most of it is starch and a small amount is sugar.
                            </p><br>
                            <p>The glycemic index of white rice is estimated to be 73. The glycemic index of brown rice, on the other hand, is estimated to be about 68. Short-grain rice tends to have a higher glycemic index than long-grain, medium-grain, and brown rice.</p><br>
                            <p>Brown rice also has fewer carbohydrates than white rice at 49.6g per cup<p>
                            <p>All rice is originally whole-grain, with the bran attached. White rice is produced by pearling, which is a process through which the grain passes through a machine where it is rolled and the bran is gently "pearled" off, leaving the white kernel intact. This makes the grain a processed, refined grain, no longer a whole grain. Pearling also lowers the cooking time and extends the shelf life of grains.</p><br>
                            <p>Rice can serve as a good source of B vitamins (including thiamin, niacin, and riboflavin) and iron. Rice is also an excellent source of manganese and magnesium.</p>
                                   
