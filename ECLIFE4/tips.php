                                       <h2>Why fruits?</h2>
                                       <p>Fruits are nature’s marvelous gift to the humankind; indeed, they are life-enhancing medicines packed with vitamins, minerals, antioxidants and many phytonutrients (Plant-derived micronutrients). They are an absolute feast to our sight, not just because of their color and flavor but of their unique nutrient profile that helps the human body be fit, rejuvenate, and free of diseases!</p>

                                       <h3>Nutrients</h3>
                            <p>Fruits are low in calories and fat and are a source of simple sugars, fiber, and vitamins, which are essential for optimizing our health.</p><br>
                            <p>Fruits provide plenty of soluble dietary fiber, which helps to ward off cholesterol and fats from the body and to help in smooth bowel movements as well as offer relief from constipation ailments.C.</p><br>
                            <p>Fruits compose of many antioxidants such as polyphenolic flavonoids, vitamin-C, and anthocyanins. These compounds, firstly help human body protected from oxidant stress, diseases, and cancers, and secondly; assist the body in developing the capacity to fight against these ailments by boosting our immunity level. Many fruits, when compared to vegetables and cereals, have very high anti-oxidant values, which measured regarding their "Oxygen Radical Absorbent Capacity" or (ORAC).</p><br>

                            <p>Anthocyanins are flavonoid category of polyphenolic compounds found in some "blue-fruits" like blue-black grapes, mulberries, acai berry, chokeberry, blueberries, blackberries, and in many vegetables featuring blue or deep purple pigments. Consumption of fruits rich in blue pigments offers many health benefits. These compounds have potent antioxidant properties that help remove free radicals from the body, and thus provide protection against cancers, aging, and infections. A majority of these pigments in the fruits tend to concentrate just underneath their skin.</p><br>

                            <p>Fruit’s health benefiting properties are because of their richness in vitamins, minerals, micro-nutrients, pigment anti-oxidants. Altogether, these compounds help the body prevent or at least prolong the natural changes of aging by protecting from damage and rejuvenating cells, tissues, and organs.</p><br>

                            <p>Their overall benefits are manifold! Fruit nutrition benefits are enormous! The inclusion of fruits in the daily diet help you protect from minor ailments like wrinkling of skin, hair fall, and memory loss. In the long run, they help avert major diseases like age-related macular degeneration (AMRD) of the retina in the eyes, Alzheimer’s disease, colon cancers, weak bones (osteoporosis)…etc., and this list of fruit nutrition benefits simply never ends!</p>
 
                                   
